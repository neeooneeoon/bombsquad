package com.oneeightfive.bombsquad.World;

public class DestroyedBrick {
    private final int x;
    private final int y;

    public DestroyedBrick(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

}
